'''
Condicional completo (if - else) con expresión lógica simple
Puede ser una condición donde devuelva un mensaje según si se da la condición y otra si la condición no se da.
En este caso queremos que nos diga si el número el positivo o el número es negativo
'''
num=int(input("Escriba un número para determinar si es positivo o negativo: "))
if num >= 0:                                                        
    print("El número es positivo")
else:                                                           
    print("El número es negativo")